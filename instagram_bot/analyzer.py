import os

from google import genai

# Простой список негативных слов для базовой модерации (fallback)
NEGATIVE_KEYWORDS = [
    "ужасно", "плохо", "мошенники", "отвратительно", "верните", "украли", 
    "суд", "жалоба", "ужас", "кошмар", "проблема", "не работает",
    # Казахские слова
    "жаман", "нашар", "ұрлады", "соттасам", "мәселе", "жұмыс істемейді", 
    "қайтарыңдар", "алаяқтар", "масқара"
]

def is_negative_comment(text: str) -> bool:
    """
    Определяет, является ли комментарий негативным.
    Использует Gemini API если есть ключ, иначе ищет по ключевым словам.
    """
    gemini_key = os.getenv("GEMINI_API_KEY")
    if gemini_key:
        try:
            client = genai.Client(api_key=gemini_key)
            prompt = (
                f"Определи, является ли следующий комментарий негативным или жалобой на банк. "
                f"Ответь только одним словом: 'ДА' или 'НЕТ'. Комментарий: '{text}'"
            )
            response = client.models.generate_content(
                model='gemini-2.5-flash',
                contents=prompt,
            )
            return "да" in response.text.lower()
        except Exception as e:
            print(f"Ошибка при обращении к Gemini: {e}")
            # В случае ошибки падаем в fallback (поиск по словам)

    # Fallback: Простой поиск по ключевым словам
    text_lower = text.lower()
    for word in NEGATIVE_KEYWORDS:
        if word in text_lower:
            return True
            
    return False

def generate_reply(text: str) -> str:
    """
    Генерирует умный персонализированный ответ на негативный комментарий через Gemini AI.
    """
    gemini_key = os.getenv("GEMINI_API_KEY")
    if gemini_key:
        try:
            client = genai.Client(api_key=gemini_key)
            prompt = (
                "Ты — профессиональный SMM-менеджер Отбасы банка (казахстанский ипотечный банк). "
                "Клиент оставил негативный комментарий под постом в Instagram. "
                "Напиши вежливый, искренний и персонализированный ответ на этот конкретный комментарий. "
                "Требования к ответу:\n"
                "- Максимум 3 предложения (Instagram-формат, коротко!)\n"
                "- Обратись к сути жалобы, не игнорируй её\n"
                "- Вырази искреннее сочувствие\n"
                "- В конце попроси написать в Direct с ИИН и номером телефона для решения вопроса\n"
                "- Не используй слова 'уважаемый/уважаемая' — слишком формально\n"
                "- Пиши живо, по-человечески, без шаблонных фраз\n"
                "- Отвечай на том же языке что и комментарий (русский или казахский)\n\n"
                f"Комментарий клиента: '{text}'"
            )
            response = client.models.generate_content(
                model='gemini-2.5-flash',
                contents=prompt,
            )
            return response.text.strip()
        except Exception as e:
            print(f"Ошибка при обращении к Gemini для генерации ответа: {e}")

    # Fallback: стандартный шаблон если Gemini недоступен
    return (
        "Здравствуйте! Нам очень жаль, что вы столкнулись с трудностями. "
        "Пожалуйста, напишите нам в Direct ваш ИИН и номер телефона, "
        "чтобы мы могли оперативно разобраться в ситуации. С уважением, Отбасы банк."
    )


def generate_dm_reply(text: str, history_text: str = "") -> str:
    """
    Генерирует ответ на личное сообщение (DM) с помощью Gemini AI.
    Учитывает историю предыдущих сообщений.
    """
    gemini_key = os.getenv("GEMINI_API_KEY")
    if gemini_key:
        try:
            client = genai.Client(api_key=gemini_key)
            prompt = (
                "Ты — вежливый и профессиональный менеджер поддержки Отбасы банка в Instagram. "
                "Клиент написал тебе в личные сообщения. Ответь ему коротко (2-3 предложения), "
                "вежливо и по делу. Если клиент жалуется — вырази сочувствие и попроси ИИН и номер телефона. "
                "Если задаёт вопрос — постарайся помочь или направь к специалисту. "
                "Если просто здоровается — поприветствуй и спроси, чем можешь помочь. "
                "Не используй эмодзи. Отвечай на том же языке, на котором пишет клиент (русский или казахский).\n\n"
            )
            if history_text:
                prompt += f"История последних сообщений (для понимания контекста):\n{history_text}\n\n"
                
            prompt += f"Новое сообщение клиента: '{text}'\nТвой ответ:"

            response = client.models.generate_content(
                model='gemini-2.5-flash',
                contents=prompt,
            )
            return response.text.strip()
        except Exception as e:
            print(f"Ошибка при обращении к Gemini для DM: {e}")

    # Fallback: стандартный ответ
    return (
        "Здравствуйте! Спасибо за ваше обращение. "
        "Наш специалист свяжется с вами в ближайшее время. "
        "С уважением, Отбасы банк."
    )
