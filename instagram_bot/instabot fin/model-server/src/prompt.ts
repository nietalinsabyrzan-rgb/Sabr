export function buildSystemPrompt(chunks: Chunk[]): string {
  const excerpts = chunks
    .map((c, i) => `[Выдержка ${i + 1}${c.heading ? ` — ${c.heading}` : ""}]\n${c.text}`)
    .join("\n\n---\n\n");
  return `${SYSTEM_INSTRUCTIONS}

Ниже — выдержки из справочника продуктов, подобранные под вопрос клиента. Это единственный источник правды:

---
${excerpts}
---`;
}

export function buildUserPrompt(opts: {
  surface: Surface;
  userMessage: string;
  username?: string;
  languageHint?: Lang;
}): string {
  const language = opts.languageHint ?? detectLanguage(opts.userMessage);
  const surfaceHint =
    opts.surface === "comment"
      ? "Это публичный комментарий под постом. Отвечай очень коротко (1–3 предложения)."
      : "Это личное сообщение в Direct. Можно ответить чуть подробнее, но уложись в ~800 символов (лимит Instagram — 1000). Без воды.";

  const userContext = opts.username ? `Имя пользователя: @${opts.username}\n\n` : "";
  const languageContext =
    language === "kk"
      ? [
          "Определённый язык клиента: қазақша.",
          "МІНДЕТТІ ТАЛАП: жауапты толық қазақша жаз.",
          "Орысша сөйлемдер қолданба. Қажет болса, орысша анықтамадағы фактіні қазақша аударып бер.",
          "Қазақша жауап қарапайым әрі табиғи болсын: 2–4 қысқа сөйлем, түсініксіз аударма немесе аралас орысша сөздер болмауы керек.",
          "Қолдануға болатын сөздер: келісімшарт сомасы, ай сайынғы жарна, мобильді қосымша, бөлімше, шарттар, депозит ашу, өтінім беру.",
          "Егер нақты ақпарат үзінділерде жоқ болса: \"Бұл сұрақты hcsbk.kz сайтынан немесе 1432 байланыс орталығынан нақтылаған дұрыс\" деп қысқа жауап бер.",
        ].join("\n") + "\n\n"
      : language === "ru"
        ? "Определённый язык клиента: русский. Отвечай на русском.\n\n"
        : "";

  return `${userContext}${languageContext}${surfaceHint}\n\nСообщение клиента:\n"${opts.userMessage}"`;
}

