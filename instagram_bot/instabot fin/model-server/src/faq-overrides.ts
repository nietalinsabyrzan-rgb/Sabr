export function matchFaqOverride(
  text: string,
  language: Lang,
  surface: Surface,
): { id: string; reply: string } | undefined {
  const normalized = text.toLocaleLowerCase("ru-RU").replace(/ё/g, "е");
  const match = FAQ_OVERRIDES.find((item) =>
    item.patterns.some((pattern) => pattern.test(normalized)),
  );
  if (!match) return undefined;
  return { id: match.id, reply: match.reply[language][surface] };
}
