export function isGreetingOnly(text: string): boolean {
  const words = text
    .toLocaleLowerCase("ru-RU")
    .replace(/ё/g, "е")
    .match(/[\p{L}]+/gu);
  if (!words || words.length === 0 || words.length > 4) return false;
  return words.every((word) => GREETING_WORDS.has(word));
}
